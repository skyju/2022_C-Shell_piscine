char	*ft_strupcase(char *str)
{	
	int	i;
	int	x;

	x = 'a' - 'A';
	i = 0;
	while (*(str + i))
	{
		if (*(str + i) >= 'a' && *(str + i) <= 'z')
			*(str + i) -= x;
		++i;
	}
	return (str);
}
