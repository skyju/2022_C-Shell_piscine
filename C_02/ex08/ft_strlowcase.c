char	*ft_strlowcase(char *str)
{
	int	i;
	int	x;

	x = 'a' - 'A';
	i = 0;
	while (*(str + i))
	{
		if (*(str + i) >= 'A' && *(str + i) <= 'Z')
			*(str + i) += x;
		++i;
	}
	return (str);
}
