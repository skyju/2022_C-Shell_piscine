#include <unistd.h>

void	ft_putnbr(int nb)
{	
	char	temp;

	if (nb == -2147483648)
	{
		write(1, "-2147483648", 1);
		return ;
	}
	if (nb < 0)
	{
		write(1, "-", 1);
		nb *= -1;
	}
	if (nb / 10 != 0)
		ft_putnbr(nb / 10);
	else
	{
		temp = nb + '0';
		write(1, &temp, 1);
		return ;
	}
	ft_putnbr(nb % 10);
}
