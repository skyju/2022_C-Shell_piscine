int	do_op_plus(int v1, int v2)
{
	return (v1 + v2);
}

int	do_op_minus(int v1, int v2)
{
	return (v1 - v2);
}

int	do_op_div(int v1, int v2)
{
	return (v1 / v2);
}

int	do_op_mod(int v1, int v2)
{
	return (v1 % v2);
}

int	do_op_multiply(int v1, int v2)
{
	return (v1 * v2);
}
